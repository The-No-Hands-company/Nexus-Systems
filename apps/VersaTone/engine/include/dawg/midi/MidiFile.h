#pragma once
#include <string>
#include <vector>
#include <cstdint>

namespace dawg {
    struct MidiEvent {
        uint32_t tick;
        uint8_t status;
        std::vector<uint8_t> data;
    };

    struct MidiTrack {
        std::vector<MidiEvent> events;
    };

    struct MidiFile {
        uint16_t format;
        uint16_t nTracks;
        uint16_t division;
        std::vector<MidiTrack> tracks;
    };

    bool importMidi(const std::string& path, MidiFile& midi);
    bool exportMidi(const std::string& path, const MidiFile& midi);
}

