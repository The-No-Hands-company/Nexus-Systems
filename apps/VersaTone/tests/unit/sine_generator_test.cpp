#include "../include/SineGen.h"
#include "../include/AudioBuffer.h"
#include <cassert>
#include <cmath>
#include <iostream>

// Test: Sine wave generator should produce correct amplitude at known points
void test_sine_amplitude() {
    const double freq = 1000.0;
    const double sampleRate = 48000.0;
    const double amplitude = 0.5;
    AudioBuffer buffer(1, 48); // 1 ms
    generateSine(buffer, freq, sampleRate, amplitude);
    // At t=0, sin(0)=0
    assert(std::abs(buffer.sample(0, 0)) < 1e-12);
    // At t=sampleRate/(4*freq), sin(pi/2)=1
    size_t quarter = static_cast<size_t>(sampleRate / (4 * freq));
    assert(std::abs(buffer.sample(0, quarter) - amplitude) < 1e-6);
}

int main() {
    test_sine_amplitude();
    std::cout << "All DSP tests passed.\n";
    return 0;
}

