#pragma once

namespace dawg {
namespace core {

class Transport {
public:
    double position = 0.0; // current playhead position (seconds or beats)
    bool playing = false;
    bool looping = false;
    double loopStart = 0.0, loopEnd = 0.0;
    double tempo = 120.0;
    int timeSigNum = 4, timeSigDen = 4;

    void play() { playing = true; }
    void stop() { playing = false; }
    void seek(double pos) { position = pos; }
    void setLoop(double start, double end) { loopStart = start; loopEnd = end; looping = true; }
    void disableLoop() { looping = false; }
};

} // namespace core
} // namespace dawg

